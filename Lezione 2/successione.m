    % N[Part[RSolve[{a[n + 3] == a[n + 2] + a[n], a[1] == 1, a[2] == 1, a[3] == 2}, a[n], n][[1]][[1]], 2], 20]	
	funct = @(n) (0.19425400402459374079 + 0.12254969128779742656*1i)*(-0.23278561593838401333 - 0.79255199251544784833*1i)^n + ...
	(0.19425400402459374079 - 0.12254969128779742656*1i)*(-0.23278561593838401333 + 0.79255199251544784833*1i)^n + ...
	0.61149199195081251841 * 1.4655712318767680267^n;
	
	for i=[start:m]
		retval(end+1) = round(funct(i));
    end
end
